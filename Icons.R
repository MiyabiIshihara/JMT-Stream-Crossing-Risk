icon <- function(url, retinaUrl) {
  return(
    makeIcon(
      iconUrl = url,
      iconRetinaUrl = retinaUrl,
      shadowUrl = "www/icon_shadow.png",
      iconHeight = 35,
      iconWidth = 25,
      shadowHeight = 35,
      shadowWidth = 35,
      iconAnchorX = 12,
      iconAnchorY = 35,
      shadowAnchorX = 6,
      shadowAnchorY = 32,
      popupAnchorX = 1,
      popupAnchorY = -35
    )
  )
}

